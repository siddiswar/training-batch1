package conditionalstatements;

public class IfStatements {

	public static void main(String[] args) {

		int a=15;
		int b=15;
		
		if(a>b){
			System.out.println("a is greater than b");
		}
		
		if(a<b){
			System.out.println("a < b");
		}
		
		if(a==b){
			System.out.println("a=b");
		}
	}

}
